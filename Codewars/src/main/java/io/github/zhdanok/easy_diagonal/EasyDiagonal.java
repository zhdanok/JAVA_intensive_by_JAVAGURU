package io.github.zhdanok.easy_diagonal;

public class EasyDiagonal {

    private int a;
    private int b;

    public EasyDiagonal() {
    }

    public EasyDiagonal(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }
}
