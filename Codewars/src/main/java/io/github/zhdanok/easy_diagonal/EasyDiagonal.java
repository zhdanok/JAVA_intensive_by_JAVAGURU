package io.github.zhdanok.easy_diagonal;

public class EasyDiagonal {

    private int n;
    private int p;

    public EasyDiagonal() {
    }

    public EasyDiagonal(int n, int p) {
        this.n = n;
        this.p = p;
    }

    public int getN() {
        return n;
    }

    public void setN(int n) {
        this.n = n;
    }

    public int getP() {
        return p;
    }

    public void setP(int p) {
        this.p = p;
    }
}
